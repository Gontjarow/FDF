#include "fdf.h"

t_xyz	vector_add(t_xyz a, t_xyz b)
{
	return ((t_xyz){
		(a.y * b.z) - (a.z * b.y),
		(a.x * b.z) - (a.z * b.x),
		(a.x * b.y) - (a.y * b.x)
	});
}
